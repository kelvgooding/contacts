/* Create DB Table */

CREATE TABLE contacts (
    first_name VARCHAR(255),
    last_name VARCHAR(255),
    number VARCHAR(15),
    mailbox VARCHAR(255),
    address VARCHAR(255),
    town VARCHAR(255),
    postcode VARCHAR(10),
    birthday VARCHAR(10),
    gender VARCHAR(1),
    social_ig VARCHAR(255),
    grp VARCHAR(255),
    added_on VARCHAR(255)
);
